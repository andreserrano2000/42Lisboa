#include <unistd.h>

void	ft_print_combn(int n)
{
	long int	a;
	long int	b;
	char		c;

	if (n > 0 && n < 10)
	{
		a = (long int)n;
		b = 0;
		while (a > 0)
		{
			b = (10 * b) + a % 10;
			a /= 10;
		}
		while (b > 0)
		{
			c = '0' + (b % 10);
			write(1, &c, 1);
			b /= 10;
		}
	}
}
