int		ft_sqrt(int nb)
{
	long long res;

	res = 1;
	if (nb == 1)
		return (1);
	if (nb <= 0)
		return (0);
	while (res * res != nb)
	{
		res++;
		if (res > (nb / 2))
			return (0);
	}
	return (res);
}
