#include <unistd.h>

void	ft_print_comb(void)
{
	char x[5];

	x[0] = '0' - 1;
	x[3] = ',';
	x[4] = ' ';
	while (++x[0] <= '7')
	{
		x[1] = '0' - 1;
		while (++x[1] <= '8')
		{
			x[2] = '0' - 1;
			while (++x[2] <= '9')
			{
				if ((x[0] != (x[1] && x[2])) && (x[1] != x[2])
					&& (x[0] < x[1]) && (x[1] < x[2]))
				{
					if ((x[0] == '7') && (x[1] == '8') && (x[2] == '9'))
						write(1, &x, 3);
					else
						write(1, &x, 5);
				}
			}
		}
	}
}
